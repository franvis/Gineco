private void lblNuevaObraSocialMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblNuevaObraSocialMouseClicked
    if (this.txtNombreObraSocial.getText().isEmpty()) {
        MensajesValidaciones.mostrarError(this, "Ingrese un nombre de obra social v치lido...");
        return;
    }
    if(!esModificar){
        if (daoObraSocial.registrarObraSocial(new ObraSocial(0, txtNombreObraSocial.getText()))) {
            MensajesValidaciones.mostrarInformacion(this, "Registro Exitoso.");
            this.txtNombreObraSocial.setText("");
            this.obras.removeAll(obras);
            this.obras = daoObraSocial.getAllObrasSociales();
            llenarObrasSociales(obras);
        } else {
            MensajesValidaciones.mostrarError(this, "Registro Fallido.");
        }
    }
    else
    {
        int idObraSeleccionada = obras.get(tblObrasSociales.getSelectedRow()).getId();
        if(daoObraSocial.modificarObraSocial(new ObraSocial(idObraSeleccionada,txtNombreObraSocial.getText())))
        {
        MensajesValidaciones.mostrarInformacion(this, "Actualizaci칩n Exitosa.");
            this.txtNombreObraSocial.setText("");
            this.obras.removeAll(obras);
            this.obras = daoObraSocial.getAllObrasSociales();
            llenarObrasSociales(obras);
        } else {
            MensajesValidaciones.mostrarError(this, "Actualizaci칩n Fallida.");
        }
    }
}//GEN-LAST:event_lblNuevaObraSocialMouseClicked

