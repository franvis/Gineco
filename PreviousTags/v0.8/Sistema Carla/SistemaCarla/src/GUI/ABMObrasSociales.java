    private void llenarObrasSociales(LinkedList<ObraSocial> obras) {
        Object[] o;
        this.dtmObras = (DefaultTableModel) this.tblObrasSociales.getModel();
        this.borrarFilas(dtmObras);
        for (int i = 0; i < obras.size(); i++) {
            o = new Object[1];
            o[0] = obras.get(i).getNombre();
            dtmObras.addRow(o);
        }
        this.tblObrasSociales.changeSelection(0, 0, false, false);
    }
    private void borrarFilas(DefaultTableModel dtmObras) {
        int j = dtmObras.getRowCount();
        for (int i = 0; i < dtmObras.getRowCount();) {
            dtmObras.removeRow(0);
        }
    }
    
    private void setearLabels(JLabel jlbl, boolean entrada){
    if(!obras.isEmpty() || (jlbl == this.lblVolverPrincipal || jlbl == this.lblNuevaObraSocial)){
        if (entrada) {
            jlbl.setEnabled(true);
            jlbl.setFont(new java.awt.Font("Tahoma", 1, 13));
            jlbl.setForeground(new java.awt.Color(0, 51, 102));
        }
        else {
            jlbl.setEnabled(false);
            jlbl.setFont(new java.awt.Font("Tahoma", 1, 12));
            jlbl.setForeground(new java.awt.Color(0, 0, 0));
        }
    }
    }
    
